class Solution {
    public boolean isPalindrome(int x) {
        
        int original = x;
        int count = 0;

        while(x > 0)
        {
            int digit = x % 10;
            count = (count*10) + digit;
            x = x / 10;
        }
        if(count == original)
        {
            return true;
        }
        return false;
    }
}